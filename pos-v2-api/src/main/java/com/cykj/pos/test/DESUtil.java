        // String uuid = UUID.randomUUID().toString().replace("-","");
        String str = "123456";
        // 加密
        for(int i=0;i<10;i++){
            String str1 = encrypt("1234567"+i,str);
            System.out.println("i="+i+",str1="+str1);
        }


        // 解密
       /* String str1 = decrypt("111222333",str);
        System.out.println("str1="+str1);*/
